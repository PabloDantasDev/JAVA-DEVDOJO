package ArraysMULTIDIMENSIONAIS;

class Aula01IntroduçãoArraysMultidimensionais {

	public static void main(String[] args) {
		int [][] ArrayCom2DimiDimensao = new int [2][2];
		ArrayCom2DimiDimensao[0][0]=25;
		ArrayCom2DimiDimensao[0][1]=20;
		ArrayCom2DimiDimensao[1][0]=35;
		ArrayCom2DimiDimensao[1][1]=80;
		
		System.out.println(ArrayCom2DimiDimensao[0][1]+ArrayCom2DimiDimensao[1][1]);
	}

}
